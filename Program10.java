/*
 * Unary Operator
 * Unary Plus, minus
 * increment -> prefix increment, postfix increment
 * decrement -> prefix decrement, postfix decrement
 */
public class Program10 {
  public static void main(String[] args) {
    int x = 10;
    System.out.println(+x);
    System.out.println(-x);
    System.out.println(x++);
    System.out.println(++x);
    System.out.println(x);
    System.out.println(x--);
    System.out.println(--x);
  }
}
