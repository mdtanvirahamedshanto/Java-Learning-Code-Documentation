public class Program8 {
  public static void main(String[] args) {
   int x = 10;
   x += 1;
   System.out.println("x = "+x);
   x -= 1;
   System.out.println("x = "+x);
   x *= 1;
   System.out.println("x = "+x);
   x /= 1;
   System.out.println("x = "+x);
   x %= 1;
   System.out.println("x = "+x);
  }
}
